"""Smoke + sanity tests for the structured-extraction synthetic datasets.

Covers the five query-conditioned generators from
``docs/mixed_decoder/data_extraction/autogenerated_plan.md`` §2:

* ``keyval``    -> :class:`KeyValRecallDataset`     (key-value recall)
* ``jsonfield`` -> :class:`JsonFieldRecallDataset`  (JSON field extraction)
* ``jsonata``   -> :class:`JsonataRecallDataset`    (JSONata/jq selection + transform)
* ``xmlxpath``  -> :class:`XmlXpathRecallDataset`   (XML/XPath extraction)
* ``sql``       -> :class:`SqlSelectRecallDataset`  (SQL selection/aggregate)

All five wrap a HuggingFace ``text`` dataset and emit the same
:class:`MaskedCiteBatch` shape as the ``Cite`` task, so they route through
``MixedDecoder.run_on_text_citation`` unchanged. The tests verify, for every
generator:

1. ``get_batch`` returns a well-formed ``MaskedCiteBatch`` (consistent shapes,
   long tensors, non-empty prompts and targets).
2. The answer is *grounded in the encoded record* for the verbatim families
   (selection-only generators), proving the needle lives in the context.
3. A real ``MixedDecoder`` forward + backward produces a finite loss and
   non-zero gradients.
"""
import numpy as np
import pytest
import torch
from datasets import Dataset
from transformers import AutoTokenizer

from mllm.config.model import (
    BertEmbType, EncBertCfg, MixedDecoderCfg, MixedDecoderDsType,
    MixedDecoderTrainCfg, MixedDecoderType,
)
from mllm.train.encdec_graph_bert import MaskedCiteBatch
from mllm.model.mixed_decoder import MixedDecoder

from mllm.train.key_val_recall import KeyValRecallDataset, KeyValRecallCfg
from mllm.train.json_field_recall import JsonFieldRecallDataset, JsonFieldRecallCfg
from mllm.train.jsonata_recall import JsonataRecallDataset, JsonataRecallCfg
from mllm.train.xml_xpath_recall import XmlXpathRecallDataset, XmlXpathRecallCfg
from mllm.train.sql_select_recall import SqlSelectRecallDataset, SqlSelectRecallCfg


DEVICE = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
BERT_NAME = 'bert-base-uncased'
INP_LEN = 64
MAX_SEQ_LEN = 192
BATCH_SIZE = 4
D_MODEL = 768
SEED = 1234


# A handful of word-rich passages: the generators sample real words from these
# for keys/values, so they need enough distinct vocabulary to fill a record.
TEXTS = [
    'The Amazon rainforest is a moist broadleaf tropical forest in the Amazon biome '
    'that covers most of the Amazon basin of South America. This region includes '
    'territory belonging to nine nations and 3,400 protected indigenous territories.',
    'Photosynthesis is a process used by plants and other organisms to convert light '
    'energy into chemical energy that can later be released to fuel the organism '
    'activities through cellular respiration inside the green chloroplasts of leaves.',
    'The Great Wall of China is a series of fortifications that were built across the '
    'historical northern borders of ancient Chinese states and Imperial China as '
    'protection against various nomadic groups from the Eurasian Steppe over centuries.',
    'Jupiter is the fifth planet from the Sun and the largest in the Solar System. '
    'It is a gas giant with a mass more than two and a half times that of all the '
    'other planets in the Solar System combined, but slightly less than the Sun.',
    'The Renaissance was a fervent period of European cultural, artistic, political '
    'and economic rebirth following the Middle Ages, generally described as taking '
    'place from the 14th century to the 17th century across many Italian cities.',
    'Antarctica is the southernmost continent and site of the South Pole. It is a '
    'virtually uninhabited, ice-covered landmass surrounded by the Southern Ocean and '
    'contains the largest single mass of freshwater ice on the planet today.',
]


# Registry: (id, dataset_class, cfg, is_verbatim)
# ``is_verbatim`` marks selection-only generators whose answer is copied straight
# from the record. The jsonata/sql generators also emit Tier-C aggregate queries
# (count/sum/max) whose answer is *computed* and need not appear verbatim.
DATASET_SPECS = [
    pytest.param(KeyValRecallDataset, KeyValRecallCfg(min_pairs=4, max_pairs=8), True, id='keyval'),
    pytest.param(JsonFieldRecallDataset, JsonFieldRecallCfg(min_fields=4, max_fields=8), True, id='jsonfield'),
    pytest.param(JsonataRecallDataset, JsonataRecallCfg(min_fields=4, max_fields=8), False, id='jsonata'),
    pytest.param(XmlXpathRecallDataset, XmlXpathRecallCfg(min_nodes=4, max_nodes=8), True, id='xmlxpath'),
    pytest.param(SqlSelectRecallDataset, SqlSelectRecallCfg(min_rows=4, max_rows=8), False, id='sql'),
]


@pytest.fixture(scope='module')
def tkz():
    return AutoTokenizer.from_pretrained(BERT_NAME)


@pytest.fixture(scope='module')
def hf_ds():
    return Dataset.from_dict({'text': TEXTS})


@pytest.fixture(scope='module')
def model(tkz):
    """A single MixedDecoder reused across generators: all of them emit
    MaskedCiteBatch and route through run_on_text_citation regardless of the
    configured ds-type, so one model exercises every generator."""
    cfg = MixedDecoderCfg(
        enc_bert=EncBertCfg(
            inp_len=INP_LEN, d_model=D_MODEL, pad_token_id=tkz.pad_token_id,
            pretrained_model_name=BERT_NAME, tokenizer_name=BERT_NAME, emb_type=BertEmbType.Cls,
        ),
        decoder_type=MixedDecoderType.BertDec,
        decoder_model_name=BERT_NAME,
        max_seq_len=MAX_SEQ_LEN,
        use_sep=True,
        prompt_all=False,
        emb_exp_rate=4,
        emb_win_min_size=2,
        emb_win_max_size=BATCH_SIZE,
        d_model=D_MODEL,
        train_ds_type=MixedDecoderDsType.Cite,
        train_cfg=MixedDecoderTrainCfg(freeze_encoder=False),
    )
    m = MixedDecoder(cfg, tkz, tkz)
    m.to(DEVICE)
    m.train()
    return m


def _build_dataset(ds_cls, cfg, tkz, hf_ds):
    ds = ds_cls(
        hf_ds, tkz, max_seq_len=MAX_SEQ_LEN, cfg=cfg, device=DEVICE, tkz_dec=tkz, seed=SEED,
    )
    return ds


def _decode(tkz, toks_row, att_row=None):
    ids = toks_row.tolist()
    if att_row is not None:
        ids = [t for t, a in zip(ids, att_row.tolist()) if a == 1]
    return tkz.decode(ids, skip_special_tokens=True).strip()


# ---------------------------------------------------------------------------
# 1. Structural validity of the emitted batch
# ---------------------------------------------------------------------------

@pytest.mark.parametrize('ds_cls, cfg, is_verbatim', DATASET_SPECS)
def test_batch_is_well_formed(ds_cls, cfg, is_verbatim, tkz, hf_ds):
    ds = _build_dataset(ds_cls, cfg, tkz, hf_ds)
    assert len(ds) == len(TEXTS)

    inds = list(range(BATCH_SIZE))
    batch = ds.get_batch(inds)

    assert isinstance(batch, MaskedCiteBatch)

    # Long, 2-D tensors with a consistent batch dimension.
    for name in ('inp_toks', 'inp_masked_toks', 'prompts_toks', 'cites_toks',
                 'cites_masked_toks', 'inp_att_mask', 'prompts_att_mask', 'cites_att_mask'):
        t = getattr(batch, name)
        assert torch.is_tensor(t), f'{name} is not a tensor'
        assert t.dtype == torch.long, f'{name} dtype is {t.dtype}, expected long'
        assert t.dim() == 2, f'{name} is not 2-D'
        assert t.shape[0] == BATCH_SIZE, f'{name} batch dim {t.shape[0]} != {BATCH_SIZE}'

    # Attention masks match their token tensors and select a non-empty span.
    for toks_name, att_name in (('inp_toks', 'inp_att_mask'),
                                ('prompts_toks', 'prompts_att_mask'),
                                ('cites_toks', 'cites_att_mask')):
        toks, att = getattr(batch, toks_name), getattr(batch, att_name)
        assert toks.shape == att.shape, f'{toks_name}/{att_name} shape mismatch'
        assert (att.sum(dim=1) > 0).all(), f'{att_name} has an all-zero row'

    # edge_inds links each sample to the shared "query" node (col 1 == batch_size).
    assert batch.edge_inds.shape == (2, BATCH_SIZE)

    # One TokensSubsetV2 per sample, each with a real prompt and target.
    assert len(batch.tokens_subsets) == BATCH_SIZE
    for s in batch.tokens_subsets:
        assert s.prompt and s.prompt.strip(), 'empty prompt'
        assert len(s.toks_prompt) > 0, 'empty prompt tokens'
        assert len(s.toks_cite_dec) > 0, 'empty target tokens'


# ---------------------------------------------------------------------------
# 2. The answer is grounded in the encoded record (selection generators)
# ---------------------------------------------------------------------------

@pytest.mark.parametrize('ds_cls, cfg, is_verbatim', DATASET_SPECS)
def test_answer_grounded_in_record(ds_cls, cfg, is_verbatim, tkz, hf_ds, capsys):
    ds = _build_dataset(ds_cls, cfg, tkz, hf_ds)
    inds = list(range(BATCH_SIZE))
    batch = ds.get_batch(inds)

    grounded = 0
    samples = []
    for i in range(BATCH_SIZE):
        record = _decode(tkz, batch.inp_toks[i], batch.inp_att_mask[i])
        prompt = _decode(tkz, batch.prompts_toks[i], batch.prompts_att_mask[i])
        target = _decode(tkz, batch.cites_toks[i], batch.cites_att_mask[i])
        samples.append((prompt, target, record))
        if target and target in record:
            grounded += 1

    # Always-true invariants: targets are non-empty.
    for prompt, target, record in samples:
        assert target, f'empty target. prompt={prompt!r}'

    if is_verbatim:
        # Every selection answer must be copied verbatim from the record.
        assert grounded == BATCH_SIZE, (
            f'{ds_cls.__name__}: only {grounded}/{BATCH_SIZE} answers grounded in record. '
            f'samples={samples}'
        )

    # Surface a sample for visual inspection under `pytest -s`.
    with capsys.disabled():
        p, t, r = samples[0]
        print(f'\n[{ds_cls.__name__}] grounded={grounded}/{BATCH_SIZE}')
        print(f'  record: {r[:200]}')
        print(f'  prompt: {p}')
        print(f'  target: {t}')


# ---------------------------------------------------------------------------
# 3. A real MixedDecoder forward + backward works on every generator
# ---------------------------------------------------------------------------

@pytest.mark.parametrize('ds_cls, cfg, is_verbatim', DATASET_SPECS)
def test_forward_backward_finite(ds_cls, cfg, is_verbatim, tkz, hf_ds, model):
    ds = _build_dataset(ds_cls, cfg, tkz, hf_ds)
    batch = ds.get_batch(list(range(BATCH_SIZE)))

    model.zero_grad(set_to_none=True)
    loss_dict, logits = model(batch)
    loss = loss_dict['loss']
    assert torch.isfinite(loss), f'{ds_cls.__name__}: loss is not finite ({loss})'

    loss.backward()
    n_with_grad = sum(
        1 for p in model.parameters()
        if p.grad is not None and torch.isfinite(p.grad).all() and p.grad.abs().sum() > 0
    )
    assert n_with_grad > 0, f'{ds_cls.__name__}: no parameters received gradients'


# ---------------------------------------------------------------------------
# 4. The dataloader keeps producing valid batches and shuffles cleanly
# ---------------------------------------------------------------------------

@pytest.mark.parametrize('ds_cls, cfg, is_verbatim', DATASET_SPECS)
def test_dataloader_iterates(ds_cls, cfg, is_verbatim, tkz, hf_ds):
    ds = _build_dataset(ds_cls, cfg, tkz, hf_ds)
    ds.shuffle(seed=SEED)
    for _ in range(3):
        batch = ds.get_batch(list(range(BATCH_SIZE)))
        assert isinstance(batch, MaskedCiteBatch)
        assert batch.cites_toks.shape[0] == BATCH_SIZE
